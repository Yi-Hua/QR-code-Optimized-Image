function [Y] = Q2Q(Q,Wa)
%   1x1  =>  Wa x Wa  pixels
%    __ __ _______          ______________________________
%   |__|__|       |        |        |        |            |        
%   |__|__|       |        |   8x8  |   8x8  |            |          
%   |             |        |________|________|            |          
%   |             |   =>   |        |        |            |       
%   |_____________|        |   8x8  |   8x8  |            |      
%                          |________|________|            |                   
%                          |                              |
%                          |                      ________|
%                          |                     |        |
%                          |                     |   8x8  |
%                          |_____________________|________| 
[m,n] = size(Q);
t1 = Wa*m;
t2 = Wa*n;
Y = zeros(t1,t2);
for i = 1:m
    for j = 1:n
            Y((Wa*(i-1)+1):Wa*i,(Wa*(j-1)+1):Wa*j) =  Q(i,j);
    end
end
end